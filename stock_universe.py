def get_csi300_stocks() -> List[str]:
    """获取 CSI300 成分股列表"""
    if not CSI300_FILE.exists():
        return []

    stocks = []
    with open(CSI300_FILE) as f:
        for line in f:
            parts = line.strip().split('\t')
            if len(parts) >= 3:
                stocks.append(parts[0])  # 股票代码

    return list(set(stocks))  # 去重


