def get_sector_stocks(sector: str) -> List[str]:
    """获取指定行业的股票列表"""
    return CSI300_SECTORS.get(sector, [])


def get_stock_sector(stock_code: str) -> str:
    """根据股票代码获取所属行业"""
    for sector, stocks in CSI300_SECTORS.items():
        # 统一格式比较（去掉 SH/SZ 前缀）
        clean_code = stock_code.upper().replace("SH", "").replace("SZ", "")
        for stock in stocks:
            clean_stock = stock.replace("SH", "").replace("SZ", "")
            if clean_code == clean_stock:
                return sector
    return "其他"


def yf_code_to_qlib(yf_code: str) -> str:
    """yfinance 代码转 Qlib 格式"""
    if "." in yf_code:
        symbol, suffix = yf_code.split(".")
        if suffix == "SS":
            return f"SH{symbol}"
        elif suffix == "SZ":
            return f"SZ{symbol}"
    return yf_code


def qlib_code_to_yf(qlib_code: str) -> str:
    """Qlib 代码转 yfinance 格式"""
    code = qlib_code.upper()
    if code.startswith("SH"):
        return f"{code[2:]}.SS"
    elif code.startswith("SZ"):
        return f"{code[2:]}.SZ"
    return qlib_code
